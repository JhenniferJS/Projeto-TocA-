    /**
     * Método para ler e salvar dados na tabela referente as playlists
     */
    public void readJTablePlaylist() {
        DefaultTableModel modeloPlaylist = (DefaultTableModel) this.jTablePlaylist.getModel();
        PlaylistDAO playlistDAO = new PlaylistDAO();
        for (Playlist p : playlistDAO.buscarPlaylistCadastradas(cpf)) {
            modeloPlaylist.addRow(new Object[]{
                p.getNome(),
                p.getNumMusicas()
            });
        }
    }

    /**
     * Método para ler e salvar dados na tabela referente as musicas
     */
    public void readJTableMusica() {
        DefaultTableModel modeloMusica = (DefaultTableModel) this.jTableMusicas.getModel();
        MusicaDAO musicaDAO = new MusicaDAO();
        for (Musica m : musicaDAO.buscarMusicaSemFiltro()) {
            modeloMusica.addRow(new Object[]{
                m.getNome(),
                m.getArtista(),
                m.getGenero(),
                m.getAlbum(),
                m.getDuracao()
            });
        }
    }

