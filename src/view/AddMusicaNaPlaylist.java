    /**
     * Método para adicionar a musica na playlist
     */
    private void addMusica(){
        boolean sucesso = false;
        boolean atualizar = false;
        
        //quantidade de itens selecionados
        int rowIndex = jTablePlaylist.getSelectedRow();
        int rowIndex1 = jTableMusicas.getSelectedRow();
        
        //rowIndex = -1 -> nenhum item selecionado 
        if (rowIndex == -1) {
            JOptionPane.showMessageDialog(this, "Selecione a playlist!");
            return;
        }
        if (rowIndex1 == -1) {
            JOptionPane.showMessageDialog(this, "Selecione a musica!");
            return;
        }
        
        //conteudo dos itens selecionados
        String nomePlaylist = (String) this.jTablePlaylist.getModel().getValueAt(rowIndex, 0);
        String nomeMusica = (String) this.jTableMusicas.getModel().getValueAt(rowIndex1, 0);
        String artista = (String) this.jTableMusicas.getModel().getValueAt(rowIndex1, 1);

        PlaylistDAO playlistDAO = new PlaylistDAO();

        //adicionando a musica na playlist
        sucesso = playlistDAO.addMusicaEmPlaylist(nomeMusica, artista, nomePlaylist, cpf);
        if (sucesso) {
            JOptionPane.showMessageDialog(this, "Musica adicionada na playlist com sucesso");
            atualizar = playlistDAO.atualizaPlaylist(nomePlaylist, cpf);
            chamarTelaPrincipal();
            if(atualizar){
                JOptionPane.showMessageDialog(this, "Playlist atualizada");
            }
        } else {
            System.out.println("Tente novamente");
        }
    }
    
