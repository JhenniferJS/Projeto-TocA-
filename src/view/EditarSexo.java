    /**
     * MÃ©todo que verica e altera sexo do usuario
     */
    private void verificaSexo(){
        boolean sucesso = false;

        String sexo = null;

        UsuarioDAO userDAO = new UsuarioDAO();

        if (this.jButtonF.isSelected()) {
            sexo = "F";
        } else if (this.jButtonM.isSelected()) {
            sexo = "M";
        } else if (this.jButtonOutro.isSelected()) {
            sexo = "Nao informado";
        }

        sucesso = userDAO.atualizaSexoUsuario(cpf, sexo);
        if (sucesso) {
            JOptionPane.showMessageDialog(this, "Sexo do usuario atualizado com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Tente novamente");
        }
    }
    
